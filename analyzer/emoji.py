def analyze_emoji_sentimens(text):
    """
    Analyzes a texts sentiment based on the contained emojis. If multiple emojis are contained the average rating for all emojis in the text is returned
    """
    sum = 0.0
    count = 0
    for character in list(text):
        value = index.get(character, None)
        if value != None:
            sum += value
            count += 1
    if count == 0:
        return 0.0

    return sum/count
